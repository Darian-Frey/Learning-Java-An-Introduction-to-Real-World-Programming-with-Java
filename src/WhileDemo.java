public class WhileDemo {

    public static void main(String[] args) {

        int count = 10;
        while(count > 0) {
            System.out.println("Counting down: " + count);
            // do something useful
            // and decrement the count HA,,,Ha,,,HA!
            count = count - 1;
        }
        System.out.println("Done");
    }
}
